    private void b3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b3ActionPerformed
       try
       {
           JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Save PDF");
        int userSelection = fileChooser.showSaveDialog(this);

        if (userSelection == JFileChooser.APPROVE_OPTION) {
            String filePath = fileChooser.getSelectedFile().getAbsolutePath();
            if (!filePath.endsWith(".pdf")) 
                  filePath += ".pdf";
            Document document = new Document();
            PdfWriter.getInstance(document, new FileOutputStream(filePath));
            document.open();
             Paragraph title = new Paragraph("Transaction History",
                    FontFactory.getFont(FontFactory.HELVETICA_BOLD, 20, BaseColor.BLACK));
            title.setAlignment(Element.ALIGN_CENTER);
            document.add(title);
            document.add(Chunk.NEWLINE);

         
            PdfPTable pdfTable = new PdfPTable(t1.getColumnCount());
           for (int i = 0; i < t1.getColumnCount(); i++) {
                pdfTable.addCell(new PdfPCell(new Phrase(t1.getColumnName(i))));
            }
            for (int rows = 0; rows < t1.getRowCount(); rows++) {
                for (int cols = 0; cols < t1.getColumnCount(); cols++) {
                    Object value = t1.getValueAt(rows, cols);
                    pdfTable.addCell(value != null ? value.toString() : "");
                }
            }
              document.add(pdfTable);
            document.close();

            JOptionPane.showMessageDialog(this, "PDF downloaded successfully:\n" + filePath);
        }

       }
       catch(Exception e)
       {
           JOptionPane.showMessageDialog(this, "Error generating PDF: " + e.getMessage());
       }// TODO add your handling code here:
    }//GEN-LAST:event_b3ActionPerformed

