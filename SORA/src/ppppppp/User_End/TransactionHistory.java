     private void display() {
        DefaultTableModel model = (DefaultTableModel) t1.getModel();
        model.setRowCount(0); 

        try {
             con = DriverManager.getConnection("jdbc:mysql://localhost:3306/kkkk", "root", "pus12345");
            String sqll = "SELECT * FROM transaction WHERE sender = ? ORDER BY trans_date DESC";
            pss = con.prepareStatement(sqll);
            pss.setString(1, acc);
             rss = pss.executeQuery();

            while (rss.next()) {
                Object[] row = {
                    rss.getInt("ID"),
                    rss.getString("sender"),
                    rss.getString("Receiver"),
                    rss.getDouble("amount"),
                    rss.getString("message"),
                    rss.getString("trans_date")
                };
                model.addRow(row);
            }
             b3.setVisible(true);
            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading transactions: " + e.getMessage());
        }
    }
